const toRad = (d) => (d * Math.PI) / 180;
export function haversineKm(lat1, lng1, lat2, lng2) {
    const dLat = toRad(lat2 - lat1), dLng = toRad(lng2 - lng1);
    const a = Math.sin(dLat / 2) ** 2 + Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
    return 2 * 6371 * Math.asin(Math.min(1, Math.sqrt(a)));
}

// Distance from a point to a polyline, sampling along each segment.
export function distToPolylineKm(lat, lng, line) {
    let best = Infinity;
    for (let i = 0; i < line.length - 1; i++) {
        const [aLat, aLng] = line[i], [bLat, bLng] = line[i + 1];
        for (let t = 0; t <= 1.0001; t += 0.1) {
            const d = haversineKm(lat, lng, aLat + (bLat - aLat) * t, aLng + (bLng - aLng) * t);
            if (d < best) best = d;
        }
    }
    return best;
}

// "M5 Junction 24 Huntworth Bridgwater South" -> "M5"; "A1(M)" kept intact.
export function normaliseRoadName(raw) {
    const s = String(raw).trim().toUpperCase();
    if (s.startsWith('M6 TOLL')) return 'M6 TOLL';
    const m = s.match(/^(A\d+\(M\)|A\d+|M\d+|B\d+)/);
    return m ? m[1] : s;
}

export const parseArr = (v) => {
    if (!v) return [];
    if (Array.isArray(v)) return v;
    try { const p = JSON.parse(v); return Array.isArray(p) ? p : []; } catch { return []; }
};

